    "csv_path = os.path.join(DATA_DIR, FILENAME_CSV)\n",
    "xlsx_path = os.path.join(DATA_DIR, FILENAME_XLSX)\n",
    "\n",
    "# 3. Check which file exists and load accordingly\n",
    "if os.path.exists(csv_path):\n",
    "    df = pd.read_csv(csv_path)\n",
    "    print(f\"Loaded data from CSV: {csv_path}\")\n",
    "elif os.path.exists(xlsx_path):\n",
    "    df = pd.read_excel(xlsx_path)\n",
    "    print(f\"Loaded data from Excel: {xlsx_path}\")\n",
    "else:\n",
    "    raise FileNotFoundError(\n",
    "        f\"Neither '{FILENAME_CSV}' nor '{FILENAME_XLSX}' found in '{DATA_DIR}'\"\n",
    "    )\n",
